

def test_that_obviously_passes():

    zero_point_two = 0.2
    two_times_zero_point_one = 2 * 0.1

    assert zero_point_two == two_times_zero_point_one


def test_floating_point_arithmetics_is_hard():

    zero_point_three = 0.3
    three_times_zero_point_one = 3 * 0.1

    assert zero_point_three == three_times_zero_point_one